# convert celsius degree to fahrenheit degree
celsius = 37.5
# using formula celsius to convert fahrenheit
cel_fahre = celsius * 1.8 + 32
# print the output
print("%0.1f degree celsius is equal to %0.1f degree fahrenheit."% (celsius, cel_fahre))

# convert fahrenheit degree to celsius degree
fahren = 99.5
# using the formula fahrenheit to convert celsius.
fahr_To_Cel = (fahren - 32) / 1.8
# print the output
print("%0.1f degree fahrenheit is equal to %0.1f degree celsius." % (fahren, fahr_To_Cel))